<h3>
 
  Graduating computer science and software engineering students do not always possess the necessary skills,
  abilities, or knowledge when beginning their careers in the software industry. 
  The lack of these skills and abilities can limit the productivity of newly hired, recent graduates, or even prevent 
  them from gaining employment. 
  This paper presents the results of an empirical study where twenty-three managers and
  hiring personnel from various software companies in the United States and Europe were interviewed.
  Participants were asked about areas where recent graduates frequently struggled when beginning employment 
  at their companies and which skill deficiencies might prevent a recent graduate from being hired.
  The results of this study indicate that recent graduates struggle with using configuration management systems 
   (and other software tools), effectively communicating with co-workers and customers, producing unit tests for their code,
  and other skills or abilities. 
  The results also indicate that a lack of project experience and problem solving abilities are the most commonly cited issues 
  preventing students from gaining employment. 

